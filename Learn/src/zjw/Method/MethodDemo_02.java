package zjw.Method;

public class MethodDemo_02 {
    //学习方法的完整定义格式，并理解其调用和执行流程
    public static void main(String[] args) {
        print();
    }

    /*
     * 无参数 无返回值 类型
     */
    public static void print() {
        for (int i = 0; i < 3; i++) {
            System.out.println("hello world");
        }
    }
}
